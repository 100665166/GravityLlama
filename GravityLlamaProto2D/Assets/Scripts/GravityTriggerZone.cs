using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class GravityTriggerZone : MonoBehaviour
{
    [Tooltip("How fast should the player's transitions be? (left/right)")]
    public float playerMoveSpeed;

    private TranslateMove player;

    // ********************************************************************************************************

    void Start()
    {
        player = GameObject.FindGameObjectWithTag("Player").GetComponent<TranslateMove>();
    }

    // ========================================================================================================
    // ********************************************************************************************************
    // ========================================================================================================
    // ********************************************************************************************************
    // ========================================================================================================
    // ********************************************************************************************************
    // ========================================================================================================
    // ********************************************************************************************************
    // ========================================================================================================
    // ********************************************************************************************************
    // ========================================================================================================

    void OnTriggerEnter(Collider col)
    {
        // Are we the player?
        if (col.CompareTag("Player"))
        {
            player.sideSpeed = playerMoveSpeed;
        }
    }
}
